const util = require( '../lib/util' );
const separator = util.separator;
const log = console.log;

separator();

new Promise( ( resolve ) => setTimeout( () => resolve( 42 ), 1000 ) )
    .then( ( result ) => result / 2 )
    .then( ( result ) =>
        new Promise( ( resolve ) => setTimeout( () => resolve( result * 2 ), 3000 ) )
    ).then( ( result ) => {
        log( `The answer is: ${result}.` );

        separator();
    } );
